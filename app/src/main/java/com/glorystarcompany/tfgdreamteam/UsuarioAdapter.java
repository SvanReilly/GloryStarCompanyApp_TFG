package com.glorystarcompany.tfgdreamteam;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.recyclerview.widget.RecyclerView;


import java.util.ArrayList;
//FUNCIONANDO//
public class UsuarioAdapter extends RecyclerView.Adapter<UsuarioAdapter.UsuarioViewHolder> {
    private final Context context;
    private ArrayList<Usuario> listaUsuario;
    public UsuarioAdapter(Context context, ArrayList<Usuario> listaUsuario) {
        this.context = context;
        this.listaUsuario = listaUsuario;}
    @NonNull
    @Override
    public UsuarioViewHolder onCreateViewHolder(@NonNull ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext())
                .inflate(R.layout.ranking_item, parent, false);
        return new UsuarioViewHolder(context, view);
    }
    @Override
    public void onBindViewHolder(@NonNull UsuarioViewHolder holder, int posicion) {
        holder.BindUsuario(this.listaUsuario.get(posicion));
    }

    @Override
    public int getItemCount() {
        return this.listaUsuario.size();
    }

    public static class UsuarioViewHolder extends RecyclerView.ViewHolder {

        public TextView nombreView,  winsView, defeatsView,  rankingPosition;

        public Context context;

        public UsuarioViewHolder(Context context, View view) {
            super(view);
            this.context = context;

            rankingPosition = view.findViewById(R.id.rankingRecordView);
            nombreView = view.findViewById(R.id.rankingUsernameView);
            winsView = view.findViewById(R.id.rankingWinsView);
            defeatsView = view.findViewById(R.id.defeatsRankingView);
        }

        public void BindUsuario(Usuario usuario) {
            rankingPosition.setText(String.valueOf(getAbsoluteAdapterPosition()+1));
            nombreView.setText(usuario.getName());
            winsView.setText(String.valueOf(usuario.getWins()));
            defeatsView.setText(String.valueOf(usuario.getDefeats()));
        }
    }
}












































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































